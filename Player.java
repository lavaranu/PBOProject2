public class Player {
    
    private String nama;
    private int score;
    
    
    public Player(String nama, int score) {
        this.nama = nama;
        this.score = score;
    }


    public String getNama() {
        return nama;
    }


    public void setNama(String nama) {
        this.nama = nama;
    }


    public int getScore() {
        return score;
    }


    public void setScore(int score) {
        this.score = score;
    }

    

    

}
